package entidades;

/**
 *
 * @author aluno
 */
public class Destino {
    
    private int id;
    private String cidade;
    private String cep;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    @Override
    public String toString() {
        return "Destino{" + "id=" + id + ", cidade=" + cidade + ", cep=" + cep + '}';
    }
    
}
